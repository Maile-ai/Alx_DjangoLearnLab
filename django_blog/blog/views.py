# ---------------------------
# TAG FILTER VIEW
# ---------------------------

class PostByTagListView(ListView):
    model = Post
    template_name = "blog/post_list.html"
    context_object_name = "posts"

    def get_queryset(self):
        return Post.objects.filter(tags__slug=self.kwargs["tag_slug"]).distinct()


